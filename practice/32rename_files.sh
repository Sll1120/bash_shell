#1,$变量{变量#匹配规则}  从头开始匹配，最短删除；
echo '(1)---------------------------------------------'
demo=www.baidu.com
# 从头开始删除，*代表了所有，删除到. 这个符号结束（依靠最短匹配原则）
echo ${demo#}
echo ${demo#*.}
echo ${demo#*w}
echo ${demo#*t}
echo ${demo#*d}

#2,$(变量##匹配规则)  从头开始匹配，最长匹配（贪婪的方式）；
#什么叫贪婪匹配？
#比如这个语法，它会以贪吃蛇吃豆豆一样，一直持续匹配到第二个一样的结束符。
echo '(2)---------------------------------------------'
echo ${demo##*.}

echo '(3)---------------------------------------------'
#3,${变量%匹配规则}   从尾开始匹配，最短删除；
#4,${变量%%匹配规则}   从尾开始匹配，最长删除（贪婪模式）；
echo ${demo%%.*}
echo ${demo%.*}

