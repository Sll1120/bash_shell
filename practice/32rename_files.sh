变量				说明
${变量#匹配规则}                从头开始匹配，最短删除
${变量##匹配规则}               从头开始匹配，最长删除
${变量%匹配规则}                从尾开始匹配，最短删除
${变量%%匹配规则}               从尾开始匹配，最长删除
